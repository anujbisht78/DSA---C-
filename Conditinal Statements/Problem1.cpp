#include <iostream>
using namespace std;
int main()
{
    int a = 9;
    // both the conditions will be given in the output

    if (a == 9)
    {
        cout << "Nine" << endl;
    }
    if (a > 0)
    {
        cout << "Positive";
    }
    else
    {
        cout << "Negative";
    }
    return 0;
}